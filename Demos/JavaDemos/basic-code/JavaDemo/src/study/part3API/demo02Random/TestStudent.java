package study.part3API.demo02Random;

public class TestStudent {
    String name;
    int age;

    public TestStudent() {
    }

    public TestStudent(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
